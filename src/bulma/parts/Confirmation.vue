<template>
    <modal>
        <template #default="{ close }">
            <div class="box">
                <h5 class="subtitle is-5">
                    {{ i18n(message) }}
                </h5>
                <hr>
                <div class="level">
                    <div class="level-left"/>
                    <div class="level-right">
